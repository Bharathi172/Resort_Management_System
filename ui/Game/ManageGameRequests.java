    public void populateGameRequestTable() {
        ArrayList<GameRequest> pendingGameRequest = new ArrayList<GameRequest>();
        ArrayList<GameRequest> non_pendingGameRequest = new ArrayList<GameRequest>();

        for (GameRequest gameWRequest : system.getGameRequestDirectory().getGameRequestList()) {
            if (gameWRequest.getBookingStatus().equals("Pending")) {
                pendingGameRequest.add(gameWRequest);
            } else {
                non_pendingGameRequest.add(gameWRequest);
            }
        }

        populate_based_on_status(pendingGameRequest, non_pendingGameRequest);
    }

    public void populate_based_on_status(ArrayList<GameRequest> pendingGameRequest, ArrayList<GameRequest> non_pendingGameRequest) {
        DefaultTableModel model = (DefaultTableModel) tblGameRequests.getModel();
        model.setRowCount(0);
        for (GameRequest gameWRequest : pendingGameRequest) {

            Object[] newRow = new Object[7];
            newRow[0] = gameWRequest;
            newRow[1] = gameWRequest.getSelectedGame().getGameTitle();
            newRow[2] = gameWRequest.getSelectedGame().getGameGenre();
            newRow[3] = gameWRequest.getSelectedGame().getCost();
            newRow[4] = gameWRequest.getDurationInHours();
            newRow[5] = gameWRequest.getReservationDate();
            newRow[6] = gameWRequest.getBookingStatus();

            model.addRow(newRow);
        }

        DefaultTableModel modelNon_Pending = (DefaultTableModel) tblGameConfirmed.getModel();
        modelNon_Pending.setRowCount(0);
        for (GameRequest gameWRequest : non_pendingGameRequest) {

            Object[] newRow = new Object[7];
            newRow[0] = gameWRequest;
            newRow[1] = gameWRequest.getSelectedGame().getGameTitle();
            newRow[2] = gameWRequest.getSelectedGame().getGameGenre();
            newRow[3] = gameWRequest.getSelectedGame().getCost();
            newRow[4] = gameWRequest.getDurationInHours();
            newRow[5] = gameWRequest.getReservationDate();
            newRow[6] = gameWRequest.getBookingStatus();

            modelNon_Pending.addRow(newRow);
        }

    }
    
    public void updateWorkRequestStatus(GameRequest selectedGame, String status) {
        selectedGame.setBookingStatus(status);
        int index = 0;
        for (GameRequest gameWRequest : system.getGameRequestDirectory().getGameRequestList()) {
            if (gameWRequest.getUserId().equals(selectedGame.getUserId())) {
                system.getGameRequestDirectory().updateGameRequest(gameWRequest, index);
                break;
            }
            index++;
        }
    }

