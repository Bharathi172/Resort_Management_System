    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
         try {
            int selectedRowIndex = tblGameRequests.getSelectedRow();
            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(this, "Please select a row to approve.");
                return;
            }
            DefaultTableModel model = (DefaultTableModel) tblGameRequests.getModel();
            GameRequest selectedGameWorkRequest = (GameRequest) model.getValueAt(selectedRowIndex, 0);
            updateWorkRequestStatus(selectedGameWorkRequest, "Approved");
            ClientTransaction ct = new ClientTransaction();
            
            //User definition for email notif
            User getUser = system.getUserDirectory().getUserById(selectedGameWorkRequest.getUserId());

            float gamebooking_finalprice = selectedGameWorkRequest.getDurationInHours() * selectedGameWorkRequest.getSelectedGame().getCost();
            ct.setUserId(selectedGameWorkRequest.getUserId());
            ct.setSelectedCenter("Game Booked - " + selectedGameWorkRequest.getSelectedGame().getGameTitle() + " for " + selectedGameWorkRequest.getDurationInHours() + " hours");
            ct.setTotalCost(gamebooking_finalprice);
            system.getClientTransactionDirectory().addClientTransaction(ct);
            
             //email notification
            new EmailAlert().SendEmailOfNotification(getUser, "Game");
            
            JOptionPane.showMessageDialog(this, "Request approved successfully and email notification sent");
            populateGameRequestTable();
            formatRows();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnApproveActionPerformed

