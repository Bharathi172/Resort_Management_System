    private void populateTable() {

        try {
            DefaultTableModel model = (DefaultTableModel) scheduleTable.getModel();
            model.setRowCount(0);

            for (HouseKeepingSchedule schedule : system.getHouseKeepingScheduleDirectory().getHouseKeepingScheduleDirectory()) {

                Object[] newRow = new Object[6];
                newRow[0] = schedule.getTaskID();
                newRow[1] = schedule.getAssignedRoom();
                newRow[2] = schedule.getScheduledDate();
                newRow[3] = schedule.getTaskStatus();
                if (schedule.getAssignedUser() != null) {
                    newRow[4] = (schedule.getAssignedUser().getFirstName() + " " + schedule.getAssignedUser().getLastName());
                    newRow[5] = schedule.getAssignedUser().getUserId();
                }
                model.addRow(newRow);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    
