    private void addScheduleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addScheduleBtnActionPerformed
        // TODO add your handling code here:

        try {

            if (roomNumberTxt.getText() != null && timeTxt.getText() != null) {
                HouseKeepingSchedule newSchedule = new HouseKeepingSchedule();

//                UUID uuid = UUID.randomUUID();
//                String randomUUIDString = uuid.toString();

                // Use the next available schedule ID
                String scheduleId = String.valueOf(nextScheduleId);
                nextScheduleId++;

                String roomNumber = roomNumberTxt.getText();
                String cleaningTime = timeTxt.getText();

                Date selectedDate = dateTxt.getDate();
                SimpleDateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy");
                String strDate = dateFormat.format(selectedDate);
                Date cleaningDate = dateFormat.parse(strDate);

                newSchedule.setTaskID(scheduleId);
                newSchedule.setAssignedRoom(roomNumber);
                newSchedule.setScheduledDate(cleaningDate);
                newSchedule.setScheduledTime(cleaningTime);
                newSchedule.setTaskStatus("Pending");

                system.getHouseKeepingScheduleDirectory().getHouseKeepingScheduleDirectory().add(newSchedule);
                JOptionPane.showMessageDialog(this, "Schedule added successfully");
                populateTable();
                resetData();
            } else {
                JOptionPane.showMessageDialog(this, "Give valid inputs for adding a schedule");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Give valid inputs for adding a schedule");
        }
    }//GEN-LAST:event_addScheduleBtnActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int selectedRow = scheduleTable.getSelectedRow();

        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) scheduleTable.getModel();
            String scheduleIdToRemove = (String) model.getValueAt(selectedRow, 0);

            // Remove the schedule from the data structure
            system.getHouseKeepingScheduleDirectory().removeScheduleById(scheduleIdToRemove);

            // Remove the row from the table
            model.removeRow(selectedRow);

            JOptionPane.showMessageDialog(this, "Schedule removed successfully");
        } else {
            JOptionPane.showMessageDialog(this, "Please select a schedule to remove");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed


