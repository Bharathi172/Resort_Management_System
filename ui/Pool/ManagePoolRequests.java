    public void populatePoolRequestTable() {
        ArrayList<PoolRequest> pendingPoolRequest = new ArrayList<PoolRequest>();
        ArrayList<PoolRequest> non_pendingPoolRequest = new ArrayList<PoolRequest>();

        for (PoolRequest poolWRequest : system.getPoolRequestDirectory().getPoolRequestList()) {
            if (poolWRequest.getBookingStatus().equals("Pending")) {
                pendingPoolRequest.add(poolWRequest);
            } else {
                non_pendingPoolRequest.add(poolWRequest);
            }
        }

        populate_based_on_status(pendingPoolRequest, non_pendingPoolRequest);
    }

    public void populate_based_on_status(ArrayList<PoolRequest> pendingPoolRequest, ArrayList<PoolRequest> non_pendingPoolRequest) {
        DefaultTableModel model = (DefaultTableModel) tblPoolRequests.getModel();
        model.setRowCount(0);
        for (PoolRequest poolWRequest : pendingPoolRequest) {

            Object[] newRow = new Object[6];
            newRow[0] = poolWRequest;
            newRow[1] = poolWRequest.getSelectedPool().getSwimmingPoolTitle();
            newRow[2] = poolWRequest.getSelectedPool().getSwimmingPoolPrice();
            newRow[3] = poolWRequest.getReservationDate();
            newRow[4] = poolWRequest.getDurationInHours();
            newRow[5] = poolWRequest.getBookingStatus();

            model.addRow(newRow);
        }

        DefaultTableModel modelNon_Pending = (DefaultTableModel) tblPoolConfirmed.getModel();
        modelNon_Pending.setRowCount(0);
        for (PoolRequest poolWRequest : non_pendingPoolRequest) {

             Object[] newRow = new Object[6];
            newRow[0] = poolWRequest;
            newRow[1] = poolWRequest.getSelectedPool().getSwimmingPoolTitle();
            newRow[2] = poolWRequest.getSelectedPool().getSwimmingPoolPrice();
            newRow[3] = poolWRequest.getReservationDate();
            newRow[4] = poolWRequest.getDurationInHours();
            newRow[5] = poolWRequest.getBookingStatus();

            modelNon_Pending.addRow(newRow);
        }
    }
    
    public void updateWorkRequestStatus(PoolRequest selectedPool, String status) {
        selectedPool.setBookingStatus(status);
        int index = 0;
        for (PoolRequest poolWRequest : system.getPoolRequestDirectory().getPoolRequestList()) {
            if (poolWRequest.getUserId().equals(selectedPool.getUserId())) {
                system.getPoolRequestDirectory().updatePoolRequest(poolWRequest, index);
                break;
            }
            index++;
        }
    }

