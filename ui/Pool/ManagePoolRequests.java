    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        try {
            int selectedRowIndex = tblPoolRequests.getSelectedRow();
            if (selectedRowIndex < 0) {
                JOptionPane.showMessageDialog(this, "Please select a row to approve.");
                return;
            }
            DefaultTableModel model = (DefaultTableModel) tblPoolRequests.getModel();
            PoolRequest selectedPoolWorkRequest = (PoolRequest) model.getValueAt(selectedRowIndex, 0);
            updateWorkRequestStatus(selectedPoolWorkRequest, "Approved");
            ClientTransaction ct = new ClientTransaction();
            //User definition for email notif
            User getUser = system.getUserDirectory().getUserById(selectedPoolWorkRequest.getUserId());

            float poolbooking_finalprice = selectedPoolWorkRequest.getDurationInHours() * selectedPoolWorkRequest.getSelectedPool().getSwimmingPoolPrice();
            ct.setUserId(selectedPoolWorkRequest.getUserId());
            ct.setSelectedCenter("Pool Booked - " + selectedPoolWorkRequest.getSelectedPool().getSwimmingPoolTitle() + " for " + selectedPoolWorkRequest.getDurationInHours() + " hours");
            ct.setTotalCost(poolbooking_finalprice);
            system.getClientTransactionDirectory().addClientTransaction(ct);
            
            //email notification
            new EmailAlert().SendEmailOfNotification(getUser, "Pool");
            
            JOptionPane.showMessageDialog(this, "Request approved successfully and email notification sent");
            populatePoolRequestTable();
            formatRows();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnApproveActionPerformed

